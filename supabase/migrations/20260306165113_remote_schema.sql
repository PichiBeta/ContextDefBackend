CREATE OR REPLACE FUNCTION "public"."compute_new_embedding"("old_embedding" "public"."vector", "new_embedding" "public"."vector", "num_vectors" integer) RETURNS "public"."vector"
    LANGUAGE "sql" IMMUTABLE
    AS $$
  SELECT array(
    SELECT (a * num_vectors::real + b) / (num_vectors + 1)::real
    FROM unnest(old_embedding::real[], new_embedding::real[]) AS t(a, b)
  )::vector;
$$;


