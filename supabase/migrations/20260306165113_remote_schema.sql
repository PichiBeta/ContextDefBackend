CREATE OR REPLACE FUNCTION "public"."readings_enqueue_difficulty"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'pg_catalog', 'public'
    AS $$declare
  _url text := 'https://irspwhgeyrojqluzgciu.supabase.co/functions/v1/process-reading';
  _secret text;
  _payload jsonb;
begin
  -- Only act on a transition to 'uploaded'
  if tg_op <> 'UPDATE'
     or new.status <> 'uploaded'
     or old.status is not distinct from new.status
     or new.is_deleted = true
  then
    return new;
  end if;

  -- Mark processing synchronously inside the same transaction.
  new.status := 'processing';
  new.difficulty := null;
  new.error_message := null;

  -- Pull secret from Vault
  select decrypted_secret
    into _secret
  from vault.decrypted_secrets
  where name = 'READINGS_DIFFICULTY_WEBHOOK_SECRET'
  limit 1;

  if _secret is null then
    raise exception 'Vault secret "%" not found', 'READINGS_DIFFICULTY_WEBHOOK_SECRET';
  end if;

  _payload := jsonb_build_object(
    'reading_id', new.id,
    'language_code', new.language_code,
    'storage_path', (new.id::text),
    'content_updated_at', new.content_updated_at
  );

  -- Fire and forget (async). Actual HTTP starts after commit.
  perform net.http_post(
    url := _url,
    headers := jsonb_build_object(
      'content-type', 'application/json',
      'x-webhook-secret', _secret
    ),
    body := _payload
  );

  return new;
end;$$;


ALTER FUNCTION "public"."readings_enqueue_difficulty"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."rls_auto_enable"() RETURNS "event_trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'pg_catalog'
    AS $$
DECLARE
  cmd record;
BEGIN
  FOR cmd IN
    SELECT *
    FROM pg_event_trigger_ddl_commands()
    WHERE command_tag IN ('CREATE TABLE', 'CREATE TABLE AS', 'SELECT INTO')
      AND object_type IN ('table','partitioned table')
  LOOP
     IF cmd.schema_name IS NOT NULL AND cmd.schema_name IN ('public') AND cmd.schema_name NOT IN ('pg_catalog','information_schema') AND cmd.schema_name NOT LIKE 'pg_toast%' AND cmd.schema_name NOT LIKE 'pg_temp%' THEN
      BEGIN
        EXECUTE format('alter table if exists %s enable row level security', cmd.object_identity);
        RAISE LOG 'rls_auto_enable: enabled RLS on %', cmd.object_identity;
      EXCEPTION
        WHEN OTHERS THEN
          RAISE LOG 'rls_auto_enable: failed to enable RLS on %', cmd.object_identity;
      END;
     ELSE
        RAISE LOG 'rls_auto_enable: skip % (either system schema or not in enforced list: %.)', cmd.object_identity, cmd.schema_name;
     END IF;
  END LOOP;
END;
$$;


ALTER FUNCTION "public"."rls_auto_enable"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."temp_readings_enqueue_difficulty"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'pg_catalog', 'public'
    AS $$
declare
  _url text := 'https://irspwhgeyrojqluzgciu.supabase.co/functions/v1/calculate-difficulty';
  _secret text;
  _payload jsonb;
begin
  -- If UPDATE but content didn't change, do nothing.
  if tg_op = 'UPDATE' and new.content is not distinct from old.content then
    return new;
  end if;

  -- Mark processing synchronously inside the same transaction.
  new.status := 'processing';
  new.difficulty := null;

  -- Pull secret from Vault (supabase_vault exposes this view)
  select decrypted_secret
    into _secret
  from vault.decrypted_secrets
  where name = 'TEMP_READINGS_DIFFICULTY_WEBHOOK_SECRET'
  limit 1;

  if _secret is null then
    -- Fail fast so you notice misconfiguration immediately
    raise exception 'Vault secret "%" not found', 'TEMP_READINGS_DIFFICULTY_WEBHOOK_SECRET';
  end if;

  _payload := jsonb_build_object(
    'reading_id', new.id,
    'content', new.content
  );

  -- Fire and forget (async). Actual HTTP starts after commit.
  perform net.http_post(
    url := _url,
    headers := jsonb_build_object(
      'content-type', 'application/json',
      'x-webhook-secret', _secret
    ),
    body := _payload
  );

  return new;
end;
$$;


